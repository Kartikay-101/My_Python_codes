        # Types of Operators in Python

        # 1. Arithmetic Operators =      +, -, *, /, //, %, **
        # 2. Assignment Operators =     =, +=, -=, *=, /=, //=, %=, **=
        # 3. Comparison Operators =     ==, !=, >, <, >=, <=
        # 4. Logical Operators    =     and, or, not
        # 5. Identity Operators   =     is, is not
        # 6. Membership Operators =     in, not in
        # 7. Bitwise Operators    =     &, |, ^, ~, <<, >>