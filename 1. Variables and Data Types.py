# What is a Variable? = A variable is simply a named locations/ container that stores a value.

First_Name  = "Kartikay"
Last_Name   = "Nauityal"
Age = 21
Gmail   = "personal.mail@gmail.com"
Language    = "Python"
Phone_no = "+91 1234567891"
Is_Active   = True

print("\nVariables :- ")

print("\tFirst Name:   ", First_Name)
print("\tLast Name:    ", Last_Name)
print("\tAge:          ", Age)
print("\tGmail:        ", Gmail)
print("\tLanguage:     ", Language)
print("\tPhone_no:     ", Phone_no)
print("\tIs_Active:    ", Is_Active, "\n")

# What are Data Types? = A data type tells Python what kind of value a variable is holding.

